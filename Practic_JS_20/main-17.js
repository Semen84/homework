// Выведите в консоль шахматную доску размером 8x8, где символы чередуются (например, # и пробел). Начинайте с # в первой ячейке первой строки.
let size = 8; // размер доски
let board = "";

for (let i = 0; i < size; i++) {
  // строки
  for (let j = 0; j < size; j++) {
    // столбцы
    if ((i + j) % 2 === 0) {
      board += "#";
    } else {
      board += " ";
    }
  }
  board += "\n"; // перенос строки после каждой строки
}

console.log(board);
/* Как это работает:

Внешний цикл i — идёт по строкам (0–7)

Внутренний цикл j — идёт по столбцам (0–7)

(i + j) % 2 === 0 — проверка, чётная ли сумма индексов:

Чётная → #

Нечётная → пробел

После каждой строки добавляем \n, чтобы переходить на новую строку. */
